router = APIRouter()


@router.post("/login/access-token", response_model=schemas.Token)
def login_access_token(
    db: Session = Depends(deps.get_db),
    form_data: OAuth2PasswordRequestForm = Depends(),
) -> Any:
    """
    OAuth2 compatible token login, get an access token for future requests
    """
    user = crud.user.authenticate(
        db, email=form_data.username, password=form_data.password
    )
    if not user:
        raise HTTPException(status_code=400, detail="Incorrect email or password")
    elif not crud.user.is_active(user):
        raise HTTPException(status_code=400, detail="Inactive user")
    access_token_expires = timedelta(minutes=settings.ACCESS_TOKEN_EXPIRE_MINUTES)

    # TODO: Add tenant selection somewhere; for now use the first tenant; Discuss with Herman
    tenants = deps.get_current_user_tenants(db, user)
    tenant = tenants[0] if len(tenants) else ""

    return {
        "access_token": security.create_access_token(
            user.id, tenant, expires_delta=access_token_expires
        ),
        "token_type": "bearer",
    }


